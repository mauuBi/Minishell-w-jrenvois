#include "minishell.h"

int	ft_env(char ***env)
{
	int	i;

	i = 0;
	if (!env || !*env || !(*env)[0])
	{
		ft_putstr_fd("\n", 1);
		return (0);
	}
	while ((*env)[i])
	{
		ft_putstr_fd((*env)[i], 1);
		ft_putstr_fd("\n", 1);
		i++;
	}
	return (0);
}
